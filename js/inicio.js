function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
    document.getElementById("mySidenav").style.padding = "10vh 2vh 2vh 2vh";
    document.getElementById("body").style.marginLeft="300px";
    document.getElementById("sub-bar").style.marginLeft="250px";
}
function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("mySidenav").style.padding = "0";
    document.getElementById("body").style.marginLeft="0px";
    document.getElementById("sub-bar").style.marginLeft="0px";
}